package oop.shape;

/**
 *
 * @author genharunari
 */
public class Circle extends Shape{
    private int radius;

    public Circle(int radius) {
        this.radius = radius;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    @Override
    public float gerArea() {
        return (float) Math.PI * radius * radius;
    }
}